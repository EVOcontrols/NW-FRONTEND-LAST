import { useCallback, useState } from 'react';
import { useTranslation } from 'react-i18next';
import { toast } from 'react-toastify';
import { axiosApi } from '@entities/api';
import {
  BaseResponse,
  INews,
  INewsFilter,
  INewsSort,
  ListParams,
  PaginationResponse,
} from '@entities/types';

interface Params extends ListParams {
  sort?: INewsSort;
  filter?: INewsFilter;
}

interface ResponseDataType extends PaginationResponse {
  meetings: Array<INews>;
}

export const useGetUserMeetingsList = () => {
  const { t } = useTranslation();
  const [isLoading, setIsLoading] = useState(false);
  const [data, setData] = useState<Array<INews>>([]);
  const [total, setTotal] = useState(0);

  const getData = useCallback(
    async (params: Params) => {
      setIsLoading(true);
      try {
        const { data } = await axiosApi.get<BaseResponse<ResponseDataType>>(
          `/meetings`,
          { params },
        );
        if (data?.data?.meetings) {
          setData(data.data.meetings);
          setTotal(data.data.total_pages);
        } else {
          toast.error(t('errors.getError'));
        }
      } catch (error) {
        toast.error(t('errors.getError'));
        console.error(error);
      } finally {
        setIsLoading(false);
      }
    },
    [t],
  );

  return {
    getData,
    data,
    isLoading,
    total,
  };
};
